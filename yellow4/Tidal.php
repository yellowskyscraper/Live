				<p>Tidal is a growing collection of personal accounts concerning local access to fresh clean water. In an age where ecological cooperation is imperative to the development of a healthy lasting relationship with our planet, how we manage and think of water is vital. A general misconception of this idea is that issues of scarcity only affect developing and impoverished communities of the third world. And that anything we do here has little to no significance in changing it. The fact is that all life is connected, that the problem is here, and that there is something we all have to do. Even in Michigan, a state surrounded by 28 percent of the world's fresh water is on the tipping point, such as the long term effects of large scale irrigation and the families that go without fresh water for those months. Or Detroit's 40,000 in Highland Park that have been disconnected from their municipal water. It is in these local stories that have seemingly gone untold, the stories, the issues of our own communities that I address, gathered from the very families affected by our neglect and complacency towards policy development.</p><br>


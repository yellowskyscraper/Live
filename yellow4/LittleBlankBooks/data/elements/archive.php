			<h2>Jimventory</h2>
			<h3>Preceding Works</h3>
			<h3 class="booknumber">Book No. 80F549</h3>
		</div>
		
		<div class="right">
			<p>Have you ever stopped and thought about how many things you own, I mean really, every single thing in your everyday space and why you have them. I find that within my clutter many things go predominantly unused or even forgotten. Why do we keep so much that we don't really use or need? What compels us to hang on to the clutter? Perhaps it's more about our emotional relationship with the object than its physical attributes. These relationships can be as simple as utility or a fascination, or as complex as a memory or sentiment. Whether large or small, utilitarian or emotional, this relationship seems to prevail over the tangible object.</p>
            <br>
			<p>So, through a detailed inventory of my personal work space, I bring myself to you. This is the documentation of the relationships I have with the things I own. This is my Jimventory.</p>

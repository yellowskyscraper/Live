<h1>Tidal Project</h1>
<p>A modern day message in a bottle.</p>
<p>November 2008 | Proposal | Message In Bottle</p>

<h2>The Stories of a Swelling Fresh Water Crisis</h2>

<p>Tidal is the growing collection of stories that otherwise would have been left untold. They are the families and communities in our own back yards that are being effected by the swelling fresh water crisis. This is no longer a matter of some family over there, it's here, it's now, it's effecting all of us. Even Michigan&#151;a state surrounded by 28 percent of the worlds fresh water&#151;is on the tipping point. Seen in the long term effects of large scale irrigation and the families that go without fresh water during the summer months. Or Detroit's 40,000 in Highland Park that have been disconnected from their municipal water. Or the effects of a Nestle Bottled water plant on the environment and local communities. Or the large scale pollution of the great lakes from near by Oil Sand Refineries and Industrial dumping. It is in these local stories that have seemingly gone untold, the stories of our own communities gathered from the very families affected by the neglect and complacency towards policy development.</p>

<p>Tidal is the rising awareness in the very regions these issues affect. An awareness through a traveling Message, a Message with no intended recipient only the hope it will find an interested hand. A sort of modern message in a bottle, released, left for someone to find. Inspired from the tiny and gratifying discovery of a message inside of something, left from someone else, set into the world, eventually crossing your path. Set out into naturally occurring systems of circulation, the message would travel until interrupted by a passer by. Set out into centers where masses of people move through everyday or systems that circulate masses of people everyday in hopes of sharing a local story.</p>


<h1>Reactive Floor</h1>
<p>Connections and collaborations.</p>
<p>November 2008 | Small Scale Interactive Installation</p>

<h2>Bayer Material Sciences: <br>A Reactive Floor Prototype</h2>

<p>Bayer came to us in need of a bridge, a bridge to cross the gap between the Industrial Designer and the Material Supplier. The current chain works something like this, the Designer would create, the Engineer would have their say, then, eventually the Material Supplier would become involved. Bayer was interested in informing the designer of new material research in order to potentially influence design earlier on in the process.</p>

<p>Bayer wanted challenge the Manufacturing sector of the production line in order to inspire change and innovation through new materials and techniques. Starting within the Automotive Sector they wished to revamp an entire process. In their eyes, why replace one part to increase efficiency, when you can think holistically and improve the entire automobile drastically.</p>

<p>With collaboration in mind a series of interactive and conceptual promotions were developed to reach out into the creative sector. Ideas ranging from co-branding with artist and designers, to large interactive installations and events, to documentation and tone. Allowing Bayer to reach their hand out into the creative community in order to encourage a relationship. Promotions intended to provoke opportunities to integrate themselves into the creative process. A collaborative position would move Bayer into an active role in the ideation phase, allowing them to contribute to the problem solving process, inviting creative new solutions that might cause them to rethink or develop something entirely new. In effect, facilitating progressive change and ingenuity.</p>

<h2>Prototype 1 of 6:</h2>

<p>Extracting from the essence of collaboration and the relationships formed from such cooperation, the Reactive Floor intends to engage users in a simple collaborative interaction. This will generate an experience that allows for the user to participate and build with others. The floor visualization will grow and form around its participants, making connections between users that otherwise might not have been made. Applying the idea of multiple parts in a whole metaphorically to the users experience, reinforcing idea of collaboration. This small scale prototype would eventually be considered for installation spaces in galleries or innovation showrooms.</p>

<!--Thumbnail copy-->
<p>With collaboration in mind a series of interactive and conceptual promotions were developed so that Bayer Material Sciences could reach out into the creative sector and invite collaboration and partnership.</p>


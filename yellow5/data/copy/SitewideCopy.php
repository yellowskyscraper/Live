<h1>A Lonely Box</h1>
<p>Studying loneliness.</p>
<p>1986 - Current | Prototype | Interactive Installation</p>

<h2>What if a box could experience loneliness?</h2>

<p>Lonely Box is a part of a larger exploration of loneliness, one in a series of observations and experiments intended to better my understanding of this curious feeling.</p>

<p>In this case, I became interested in the notion that our conscious understanding of the world is limited to, or constrained to our perception of it. The idea that we perceive our surroundings, then rationalize, and it's through this that we build an understanding of our environment and our relationship to it. And since we can never truly escape these bodies of ours, and transcend this perception, we are forever trapped in a universes governed by these ever so temperamental of senses.</p>

<p>This particular exploration was to create a small electronic device that could experience loneliness on its most shallow of levels, company; alone he grows anxious and erratic, accompanied he becomes calm and ordered. The device was granted a single mode to perceive his world; movement, and a single means to expressing that perception; an illuminated and haptic heartbeat.</p>

<h2>Documentation.</h2>

<p>For this a simple algorithm was written to detect changes in the environment sent in from a Dual-Axis Accelerometer, triggering the ebb and flow of the simulated emotional reaction of a heart beat.</p>

<!--Thumbnail copy-->
<p>What if things could feel? And then if they could what feelings would they have? I gave this little box one mode to perceive, movement and orientation. And one mean with which to express itself, a heartbeat of light and vibration.</p>


<h1>ReDistraction (redis)</h1>
<p>Technology! Save us from technology!</p>
<p>April 2010 | Chrome Plug-in</p>

<h2>With time perhaps we wont need such things as these. We may be able to do them on our own someday.</h2>

<p>It all started, one day a little while back, when I noticed that every time I would open a new tab in Chrome (command+T) my fingers would automatically begin typing out the letters f a c e b o o, followed by the return key, solidifying the action, directing me to Facebook. My intent wasn't originally to check Facebook, it was perhaps something related to work, something productive! So in this, I realized, I had a problem. And that I should find help.</p>

<p>I tried will power to no avail. I thought that I might hire someone to watch over me, or find a sponsor to keep me on track. No luck. The solution struck me one evening&#151;when I was at my very lowest, sitting there facing five open tabs, all with Facebook on them&#151;that I would use technology to condition myself away from Facebook.</p>

<p>And we have it, ReDistraction was born. A simple plug-in for Chrome that detects a users navigation to Facebook, gently pushing (redirecting) the user on to something more fruitful. Something like TED, or Yale's Open Courses, or The New York Times. Anything other than Facebook.</p>

<!--Thumbnail copy-->
<p>Oh, the mountain of distraction the fathomless Internet can be. With Facebook on the very top of the mound, I sought to recover from an unhealthy addiction to this King of procrastination. This is as methadone is for heroin.</p>


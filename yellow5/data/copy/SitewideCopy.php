<h1>Little Blank Books</h1>
<p>Grab a Little Blank Book and begin.</p>
<p>May 2009 | Interactive Installation</p>

<h2>An Exhibition Concerning An Undergraduate</h2>

<p>So, what exactly is the result of an undergraduate degree in the Fine Arts and what is four years of study worth to a person these days? Is it a piece of paper noting competence in a certain specialization? Does it allow for a more significant income bracket or status? When <i>Fine Arts</i> follows it, does it then imply a cohesive body of work, thorough and not without a learned intention? Or is it rebellion and experimentation unconcerned with all that has past into convention before it? It might be silly to say that it is anything specific amongst these, in reality it's everything listed above and more, in any case at the other end of an intensive undergraduate program you have a person.</p>

<p>For me it was right there after those years of study that I found a little of myself. Where I was was no longer in those pieces&#151;valuable stepping stones no less&#151;they only seemed strange relics to me in hindsight. I had grown since then, and with that, every experimentation of the class room looked foreign, no longer a part of me. This was some thought process to find yourself in when asked to put together a Senior Exhibit, an exhibit meant to showcase four years of production and accomplished study.
</p>

<p>It was here I figured what form the showcase should take, what if it were a collection of ruminations over said intensive undergraduate career, what if instead of work it was a presentation of a personal outcome. A showcase of reflection instead of a showcase of production. It was realizing this, that I was much less concerned with what I had produced over those years of study, and much more interested with where I had come as a result of those immersed years that inspired my Little Blank Books.</p>

<h2>The Experience:</h2>

<p>Each 'Little Blank Book' in the collection is a short examination, and together they form a collection of where I landed as an undergraduate, as a person. Specifically each Little Book is blank inside, with only a code and title on the cover indicating it's contents. In order to reveal the contents of the book, the user must first place it on the a nearby reader, where the contents are then displayed on the adjoining monitors. It is through this that the user must step in order to glean anything from my experiences, it is through my own relationship with digital and physical interactions that the user must brave in order to use.</p>

<!--Thumbnail copy-->
<p>Standing there at the end of an undergraduate I found that I was no longer in the things I created throughout my studies&#151;they seemed but strange relics in hindsight. So, in stead we have a showcase of reflection, over a showcase of production.</p>


<h1>The Detroit Zoological Society</h1>
<p>The entire world at a glance.</p>
<p>November 2010 | Identity System</p>

<h2>When we try to pick out anything by itself, we find it hitched to everything else in the Universe.</h2>

<p>The Detroit Zoo is a micro representation of the Earth's five biogeographic regions, more commonly known as Biomes. Much like a map allows its participant to experience a large amount of geographic data at a glance, a Zoological Park allows a similar experience involving these five Biomes. The Detroit Zoological Park acts as a map to navigate the worlds regions at a glance, despite a vast physical separation.</p>

<p>It was in the nomenclature of maps used to describe the physical terrain and conditions of an area, where the Detroit Zoological Society's system emerged. With the inherit iconography of terrain, a method can be found to distill and represent the worlds five biogeographic realms. The five biomes represent the most basic division of the worlds surface, defined by four constants unique to a region,  climate, fauna, flora and geography.</p>

<p>The system for the Detroit Zoological Society is comprised of five graphic marks symbolizing the regions within the Park's boundaries and a logotype embodying the members of the Detroit Zoological Society; the Belle Island Aquarium, and the Zoological Park.</p>

<!--Thumbnail copy-->
<p>Identity system that describes the function of a Zoo as a sort of map with which we can navigate the world's biogeographical regions. The entire world at a glance; the tundra, forests, grasslands, deserts, and oceans!</p>

